### **Project Title:** Highly Available Web Application Deployment on AWS

# End Goal:
# Designed and implemented a scalable, highly available web application architecture 
# on AWS, ensuring reliability, performance, and fault tolerance across multiple 
# Availability Zones.

### **Key Achievements:**
Deployed a virtual network spanning multiple Availability Zones using AWS CloudFormation, 
ensuring robust connectivity and failover support.
Provisioned a highly available, fully managed relational database with Amazon RDS 
for automated failover and scalability, improving data reliability and access speed.
Implemented a database caching layer using Amazon ElastiCache to reduce query latency 
and enhance performance, providing a smoother user experience.
Configured a shared storage layer across multiple Availability Zones with Amazon EFS, 
leveraging Network File System (NFS) for consistent and reliable storage access.
Set up an Application Load Balancer for efficient traffic distribution and fault tolerance, 
enhancing application uptime and performance.
Deployed an Auto Scaling group of web servers to dynamically adjust capacity based on 
real-time demand, optimizing resource utilization and cost.
Conducted Chaos Testing using AWS Fault Injection Simulator to validate system 
resilience under failure scenarios, ensuring robustness and reliability.

### **Technologies Used:**
# - Networking & Load Balancing: Amazon VPC, Application Load Balancer (ALB)
# - Database & Caching: Amazon RDS (MySQL/PostgreSQL), Amazon ElastiCache (Memcached)
# - Storage: Amazon Elastic File System (EFS), Network File System (NFS)
# - Scalability & Resilience: Auto Scaling Groups, Launch Templates, AWS Fault Injection Simulator
# - Infrastructure as Code (IaC): AWS CloudFormation
# - Monitoring & Security: Amazon CloudWatch, AWS IAM

